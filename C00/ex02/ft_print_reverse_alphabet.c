#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	int	letter;

	letter = 123;
	while (letter >= 98)
	{
		letter--;
		ft_putchar(letter);
	}
}	
